package fr.rgary.learningcar.display;

/**
 * Class Color.
 */
public class Color {
    public double R = 0;
    public double G = 0;
    public double B = 0;
    public double A = 0;

    public Color(final double r, final double g, final double b) {
        R = r;
        G = g;
        B = b;
    }

    public Color(final double r, final double g, final double b, final double a) {
        R = r;
        G = g;
        B = b;
        A = a;
    }
}
